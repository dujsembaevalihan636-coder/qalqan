export type StatementKind = "bank" | "police";

export type StatementInput = {
  name: string;
  phone: string;
  card: string;
  bank: string;
  city: string;
  what: string;
};

export function buildStatement(
  kind: StatementKind,
  data: StatementInput,
  lang: "ru" | "kz" = "ru"
): string {
  const name = data.name.trim() || "________________";
  const phone = data.phone.trim() || "________________";
  const card = data.card.trim() || "________________";
  const bank = data.bank.trim() || "________________";
  const city = data.city.trim() || "________________";
  const what =
    data.what.trim() ||
    (lang === "kz"
      ? "Мені қаржылық схемаға тартты / карта деректерін бердім / бөтен аударымдар болды."
      : "Меня вовлекли в финансовую схему / я передал данные карты / проходили чужие переводы.");

  if (lang === "kz") {
    if (kind === "bank") {
      return `${bank} банкіне

Өтініш

Мен, ${name}, тел. ${phone}, ${city} қ.

Мынаны хабарлаймын: ${what}

Карта / шот: ${card}.

Картаны / шотты дереу бұғаттауды, күдікті операцияларды тексеруді және алаяқтық / рұқсатсыз әрекеттер туралы жүгінуді тіркеуді сұраймын. Қажет болса сөйлесу скриншоттарын және түсініктеме беремін.

Өтінемін:
1) картаны/шотты бұғаттау;
2) жүгінуді тіркеу нөмірін беру;
3) келесі қадамдарды түсіндіру.

Күні: ${new Date().toLocaleDateString("ru-KZ")}
Қолы: _______________
`;
    }
    return `Ішкі істер органына / 102

Өтініш

Мен, ${name}, тел. ${phone}, ${city} қ.

Мән-жай: ${what}

Қатысты төлем құралы: ${card} (${bank}).

Қылмыстық құқық бұзушылық белгілері бойынша (алаяқтық / төлем құралдарымен заңсыз әрекеттер) тексеру жүргізуді және өтінішті тіркеуді сұраймын. Дәлелдеме ретінде мессенджердегі сөйлесуді ұсына аламын.

Күні: ${new Date().toLocaleDateString("ru-KZ")}
Мекенжай / байланыс: ${phone}
Қолы: _______________
`;
  }

  if (kind === "bank") {
    return `В ${bank}

Заявление

Я, ${name}, тел. ${phone}, г. ${city},

сообщаю следующее: ${what}

Карта / счёт: ${card}.

Прошу незамедлительно заблокировать карту/счёт, проверить подозрительные операции и зарегистрировать обращение о мошенничестве / несанкционированных действиях. Готов(а) предоставить скриншоты переписки и дать объяснения.

Прошу:
1) заблокировать карту/счёт;
2) выдать номер обращения;
3) разъяснить дальнейшие шаги.

Дата: ${new Date().toLocaleDateString("ru-KZ")}
Подпись: _______________
`;
  }

  return `В орган внутренних дел / по линии 102

Заявление

Я, ${name}, тел. ${phone}, г. ${city},

сообщаю: ${what}

Платёжный инструмент: ${card} (${bank}).

Прошу провести проверку по признакам мошенничества / незаконных действий с платёжными инструментами и зарегистрировать заявление. В качестве доказательств готов(а) предоставить переписку в мессенджере.

Дата: ${new Date().toLocaleDateString("ru-KZ")}
Контакт: ${phone}
Подпись: _______________
`;
}
